package tareasGIT;

public class Receta {
	String nombre;
	String ingredientes;
	String preparacion;
	
	public Receta() {
		
	}
	public Receta(String nombre, String ingredientes, String preparacion) {
		this.nombre = nombre;
		this.ingredientes = ingredientes;
		this.preparacion = preparacion;
	}
	
}
